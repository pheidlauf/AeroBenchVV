function dl=dnda(alpha,beta)
a=[.001 -.027 -.017 -.013 -.012 -.016 .001 .017 .011 .017 .008 .016
.002 -.014 -.016 -.016 -.014 -.019 -.021 .002 .012 .016 .015 .011
-.006 -.008 -.006 -.006 -.005 -.008 -.005 .007 .004 .007 .006 .006
