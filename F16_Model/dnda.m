-.024 -.010 -.004 -.002 -.001 .003 .014 .006 -.001 .004 .004 .006
-.022 .002 -.003 -.005 -.003 -.001 -.009 -.009 -.001 .003 -.002 .001]';
s=.2*alpha;
k=fix(s);
if(k<=-2),k=-1;end
if(k>=9),k=8;end
da=s-k;
l=k+fix(1.1*sign(da));
s=.1*beta;
m=fix(s);
if(m<=-3),m=-2;end
